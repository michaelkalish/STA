	private CMRxProblem makeProblem() {
		CMRxGMProblemMaker maker = new CMRxGMProblemMaker();
		maker.setShrink(-1);
		maker.setGM(data);

		maker.setModel(new double[] { 1, 1, 1, 1, 1, 1 });
		int index = maker.initAdj();
		maker.addAdj(1, index, new int[] { 1, 2, 3, 4, 5, 6 });
		maker.addAdj(1, index, new int[] { 7, 8, 9, 10, 11, 12 });
		maker.addAdj(1, index, new int[] { 13, 14, 15, 16, 17, 18 });
		maker.dupeAdj(6);
		return maker.getProblem();
	}

